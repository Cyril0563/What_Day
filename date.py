url = 'https://api.ooomn.com/api/holiday?date='


with open('/date.txt', 'r') as f:
    for d in f:
        d = d.strip()
        
       
        headers = {'User-Agent': random.choice(user_agent_list)}
        
        params = {
            'date': d
        }
        
        response = requests.get(url, headers=headers, params=params)
        
        data = response.json()
        
        data = data[0]
        d = datetime.strptime(d, '%Y%m%d')
        zj = d.isoweekday()
        data_name = data['describe']
        data_name_new = data_name['Name']
